/**
 * Dynamic Programming usually has four steps to design an algorithm:<br>
 * 1. Find the characteristic of the structure of an optimal solution.<br>
 * 2. Solve the problem using recursion.<br>
 * 3. Use bottom top way to build up the solutions for sub cases.<br>
 * 4. Find the solution using calculated solution.<br>
 * <p>
 * Dynamic Programming is usually used in solving optimization problems. That is to find an optimal
 * solution, usually a minimum or a maximum, within a set of solutions. From this point of view, if
 * the problem is easy to traverse, then go from the base case and build new solution using the
 * previous solution until reach the top.<br>
 * <p>
 * It's called dynamic because in every step of calculation, either iteration or recursion, it's
 * using the result of previous calculation(s), so it's building up the solution dynamically.
 * 
 * @author Jihan Chen
 *
 */
